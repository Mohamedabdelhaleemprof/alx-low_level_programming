#include "main.h"
#include <stdio.h>

/**
 * print_to_98 - print to 98
 *
 * Return: Always n
 */
void print_to_98(int n)
{
	for (n = 0; n < 99; n++)
	{
		_putchchar('n');
		_putchchar(',');
		_putchchar(' ');
		_putchchar('\n');
	}

	for (n < 112; x = 98; x--)
	{
		_putchchar('n');
		_putchchar(',');
		_putchchar(' ');
		_putchchar('\n');
	}
	for (n = -10; x < 99; x++)
	{
		_putchchar('n');
		_putchchar(',');
		_putchchar(' ');
		_putchchar('\n');
	}
}
